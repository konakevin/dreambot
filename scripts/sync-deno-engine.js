#!/usr/bin/env node
'use strict';

/**
 * sync-deno-engine.js — Copy lib/recipeEngine.ts to the Deno edge function copy.
 *
 * Fixes imports from '@/types/recipe' to './recipe.ts' for Deno compatibility.
 *
 * Run after any changes to the recipe engine source file.
 *
 * Usage:
 *   node scripts/sync-deno-engine.js
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SRC = path.join(ROOT, 'lib/recipeEngine.ts');
const OUT = path.join(ROOT, 'supabase/functions/_shared/recipeEngine.ts');

let src = fs.readFileSync(SRC, 'utf8');

// Fix imports for Deno
src = src.replace(/from '@\/types\/recipe'/g, "from './recipe.ts'");
src = src.replace(/from "@\/types\/recipe"/g, "from './recipe.ts'");

// Add header
const header = `/**
 * Recipe Engine — DEPLOY COPY for Supabase Edge Functions (Deno runtime).
 * AUTO-GENERATED by scripts/sync-deno-engine.js — DO NOT EDIT DIRECTLY.
 * SOURCE OF TRUTH: lib/recipeEngine.ts
 */\n\n`;

fs.writeFileSync(OUT, header + src);
console.log(`✅ Synced recipeEngine.ts (${src.length} bytes) → ${path.relative(ROOT, OUT)}`);
